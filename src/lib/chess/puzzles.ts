/** Get puzzles within a rating range */
export function getPuzzlesInRange(minRating: number, maxRating: number): ChessPuzzle[] {
  return allPuzzles.filter((p) => p.rating >= minRating && p.rating <= maxRating)
}

/** Look up a puzzle by ID */
export function getPuzzleById(id: string): ChessPuzzle | undefined {
  return allPuzzles.find((p) => p.id === id)
}
